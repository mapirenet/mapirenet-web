const canvas = document.querySelector("#technicalCanvas");
const ctx = canvas ? canvas.getContext("2d") : null;
let animationFrame = 0;

// Ajusta el canvas al tamaño real de pantalla para que se vea nítido en pantallas HiDPI.
function resizeCanvas() {
  if (!canvas || !ctx) {
    return;
  }

  const ratio = window.devicePixelRatio || 1;
  canvas.width = Math.floor(canvas.offsetWidth * ratio);
  canvas.height = Math.floor(canvas.offsetHeight * ratio);
  ctx.setTransform(ratio, 0, 0, ratio, 0, 0);
}

function drawTechnicalScene() {
  if (!canvas || !ctx) {
    return;
  }

  const width = canvas.offsetWidth;
  const height = canvas.offsetHeight;
  const centerX = width * 0.72;
  const centerY = height * 0.5;
  const time = animationFrame * 0.012;

  ctx.clearRect(0, 0, width, height);

  ctx.strokeStyle = "rgba(120, 154, 190, 0.16)";
  ctx.lineWidth = 1;

  for (let x = 0; x < width; x += 48) {
    ctx.beginPath();
    ctx.moveTo(x, 0);
    ctx.lineTo(x, height);
    ctx.stroke();
  }

  for (let y = 0; y < height; y += 48) {
    ctx.beginPath();
    ctx.moveTo(0, y);
    ctx.lineTo(width, y);
    ctx.stroke();
  }

  drawGear(centerX, centerY, Math.min(width, height) * 0.18, 18, time, "rgba(47, 155, 255, 0.55)");
  drawGear(centerX - 170, centerY + 82, Math.min(width, height) * 0.105, 14, -time * 1.35, "rgba(255, 138, 53, 0.5)");
  drawDimensionLines(centerX, centerY, width, height);

  animationFrame += 1;
  requestAnimationFrame(drawTechnicalScene);
}

// Dibuja engranajes esquemáticos para dar una identidad mecánica sin usar imágenes externas.
function drawGear(x, y, radius, teeth, rotation, color) {
  const innerRadius = radius * 0.72;
  const hubRadius = radius * 0.28;
  const steps = teeth * 2;

  ctx.save();
  ctx.translate(x, y);
  ctx.rotate(rotation);
  ctx.beginPath();

  for (let i = 0; i <= steps; i += 1) {
    const angle = (Math.PI * 2 * i) / steps;
    const pointRadius = i % 2 === 0 ? radius : innerRadius;
    const px = Math.cos(angle) * pointRadius;
    const py = Math.sin(angle) * pointRadius;

    if (i === 0) {
      ctx.moveTo(px, py);
    } else {
      ctx.lineTo(px, py);
    }
  }

  ctx.closePath();
  ctx.strokeStyle = color;
  ctx.lineWidth = 2;
  ctx.stroke();

  ctx.beginPath();
  ctx.arc(0, 0, hubRadius, 0, Math.PI * 2);
  ctx.strokeStyle = "rgba(244, 247, 251, 0.42)";
  ctx.stroke();

  ctx.beginPath();
  ctx.moveTo(-radius * 1.35, 0);
  ctx.lineTo(radius * 1.35, 0);
  ctx.moveTo(0, -radius * 1.35);
  ctx.lineTo(0, radius * 1.35);
  ctx.strokeStyle = "rgba(244, 247, 251, 0.15)";
  ctx.lineWidth = 1;
  ctx.stroke();
  ctx.restore();
}

// Líneas de cota estilo plano técnico para reforzar el carácter industrial.
function drawDimensionLines(centerX, centerY, width, height) {
  ctx.strokeStyle = "rgba(244, 247, 251, 0.24)";
  ctx.fillStyle = "rgba(244, 247, 251, 0.48)";
  ctx.lineWidth = 1;
  ctx.font = "12px Arial";

  const lines = [
    [centerX - 260, centerY - 230, centerX + 210, centerY - 230, "AXIS 3D"],
    [centerX + 260, centerY - 170, centerX + 260, centerY + 210, "CONTROL"],
    [width * 0.55, height * 0.82, width * 0.9, height * 0.82, "FABRICATION"]
  ];

  lines.forEach(([x1, y1, x2, y2, label]) => {
    ctx.beginPath();
    ctx.moveTo(x1, y1);
    ctx.lineTo(x2, y2);
    ctx.stroke();
    ctx.fillText(label, x1 + 8, y1 - 8);
  });
}

window.addEventListener("resize", resizeCanvas);
if (canvas && ctx) {
  resizeCanvas();
  drawTechnicalScene();
}
